import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync } from 'node:fs';

function sh(cmd) {
  return execSync(cmd, { stdio: ['ignore', 'pipe', 'ignore'] })
    .toString()
    .trim();
}

const sha = sh('git -C .. rev-parse HEAD');
const shortSha = sh('git -C .. rev-parse --short HEAD');

const version = {
  gitSha: sha,
  gitShortSha: shortSha,
  buildTimestampUtc: new Date().toISOString(),
  buildEnv: {
    node: process.version,
    platform: process.platform,
    arch: process.arch,
    ci: Boolean(process.env.CI),
    runner: process.env.GITHUB_ACTIONS ? 'github-actions' : 'local',
  },
};

mkdirSync('public/meta', { recursive: true });
mkdirSync('src/generated', { recursive: true });

writeFileSync('public/meta/version.json', JSON.stringify(version, null, 2) + '\n', 'utf8');

// TS module for build-time imports
const ts = `// AUTO-GENERATED. Do not edit.
// Generated by site/scripts/write-version.mjs
export const buildVersion = ${JSON.stringify(version, null, 2)} as const;
`;
writeFileSync('src/generated/version.ts', ts, 'utf8');

console.log(`[version] ${shortSha} ${version.buildTimestampUtc}`);
